package demo2;

public class Sum {
    private long ans;
    private int target;

    public Sum(long ans, int target) {
        this.ans = ans;
        this.target = target;
    }

    public long getAns() {
        return ans;
    }

    public void setAns(long ans) {
        this.ans = ans;
    }

    public int getTarget() {
        return target;
    }

    public void setTarget(int target) {
        this.target = target;
    }
}
